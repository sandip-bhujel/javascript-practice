//else-if statement //
//we can use else if for multiple check or compare //
let age = 30;
if (age < 17) {
  console.log("you are very small age for now");
} else if (age < 20) {
  console.log("you are tenager so you can add others");
} else if (age < 50) {
  console.log("you are mature person and adult too ");
} else {
  console.log("you can get real citizenship ");
}

